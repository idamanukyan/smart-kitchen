-- =============================================================================
-- HELPER: updated_at TRIGGER FUNCTION
-- =============================================================================

CREATE OR REPLACE FUNCTION set_updated_at()
RETURNS TRIGGER
LANGUAGE plpgsql
AS $$
BEGIN
  NEW.updated_at = NOW();
  RETURN NEW;
END;
$$;


-- =============================================================================
-- TABLE: households
-- =============================================================================
-- A household is a group of users who share meal plans and shopping lists.
-- Created before the user profile because users.household_id references it.

CREATE TABLE IF NOT EXISTS households (
  id            UUID          PRIMARY KEY DEFAULT uuid_generate_v4(),
  name          TEXT          NOT NULL,
  size          INTEGER       NOT NULL DEFAULT 1 CHECK (size BETWEEN 1 AND 20),
  created_by    UUID          NOT NULL REFERENCES auth.users (id) ON DELETE SET NULL,
  invite_code   TEXT          NOT NULL UNIQUE,  -- 6-char alphanumeric, generated by Edge Function
  created_at    TIMESTAMPTZ   NOT NULL DEFAULT NOW(),
  updated_at    TIMESTAMPTZ   NOT NULL DEFAULT NOW()
);

COMMENT ON TABLE  households              IS 'A group of users sharing meal plans and shopping lists.';
COMMENT ON COLUMN households.size         IS 'Total number of people in the household (used for portion scaling).';
COMMENT ON COLUMN households.invite_code  IS '6-character alphanumeric code used to invite other users to this household.';
COMMENT ON COLUMN households.created_by   IS 'The user who originally created this household (household owner).';

CREATE TRIGGER households_updated_at
  BEFORE UPDATE ON households
  FOR EACH ROW EXECUTE FUNCTION set_updated_at();

-- Indexes
CREATE INDEX IF NOT EXISTS idx_households_invite_code ON households (invite_code);
CREATE INDEX IF NOT EXISTS idx_households_created_by  ON households (created_by);


-- =============================================================================
-- TABLE: users  (extends auth.users)
-- =============================================================================
-- One row per Supabase auth user. Inserted automatically via trigger on
-- auth.users INSERT (or manually after sign-up).

CREATE TABLE IF NOT EXISTS users (
  id                    UUID                  PRIMARY KEY REFERENCES auth.users (id) ON DELETE CASCADE,
  display_name          TEXT,
  household_id          UUID                  REFERENCES households (id) ON DELETE SET NULL,
  preferred_store       preferred_store_enum  NOT NULL DEFAULT 'REWE',
  onboarding_completed  BOOLEAN               NOT NULL DEFAULT FALSE,
  created_at            TIMESTAMPTZ           NOT NULL DEFAULT NOW(),
  updated_at            TIMESTAMPTZ           NOT NULL DEFAULT NOW()
);

COMMENT ON TABLE  users                       IS 'App-level user profile that extends the Supabase auth.users identity.';
COMMENT ON COLUMN users.id                    IS 'Mirrors auth.users.id — the Supabase Auth UUID.';
COMMENT ON COLUMN users.household_id          IS 'The household this user currently belongs to (nullable until household is joined or created).';
COMMENT ON COLUMN users.preferred_store       IS 'The preferred supermarket chain; affects aisle sorting and package-size rounding.';
COMMENT ON COLUMN users.onboarding_completed  IS 'Set to TRUE once the user completes all onboarding screens.';

CREATE TRIGGER users_updated_at
  BEFORE UPDATE ON users
  FOR EACH ROW EXECUTE FUNCTION set_updated_at();

-- Indexes
CREATE INDEX IF NOT EXISTS idx_users_household_id ON users (household_id);

-- Auto-create a users row when a new auth.users row is inserted
CREATE OR REPLACE FUNCTION handle_new_auth_user()
RETURNS TRIGGER
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
BEGIN
  INSERT INTO public.users (id)
  VALUES (NEW.id)
  ON CONFLICT (id) DO NOTHING;
  RETURN NEW;
END;
$$;

CREATE OR REPLACE TRIGGER on_auth_user_created
  AFTER INSERT ON auth.users
  FOR EACH ROW EXECUTE FUNCTION handle_new_auth_user();


