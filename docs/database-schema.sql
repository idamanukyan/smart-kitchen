-- =============================================================================
-- TABLE: meal_slots
-- =============================================================================
-- Each row is one meal within a meal plan (day × meal type × recipe).

CREATE TABLE IF NOT EXISTS meal_slots (
  id                UUID      PRIMARY KEY DEFAULT uuid_generate_v4(),
  meal_plan_id      UUID      NOT NULL REFERENCES meal_plans (id) ON DELETE CASCADE,
  day_of_week       SMALLINT  NOT NULL CHECK (day_of_week BETWEEN 0 AND 6),  -- 0 = Monday
  meal_type         TEXT      NOT NULL,  -- 'Frühstück' | 'Mittagessen' | 'Abendessen' | 'Abendbrot'
  recipe_id         UUID      REFERENCES recipes (id) ON DELETE SET NULL,    -- NULL = slot intentionally empty
  is_locked         BOOLEAN   NOT NULL DEFAULT FALSE,
  servings_override SMALLINT  CHECK (servings_override IS NULL OR servings_override BETWEEN 1 AND 20),
  created_at        TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at        TIMESTAMPTZ NOT NULL DEFAULT NOW(),

  -- Each (plan, day, meal_type) combination is unique
  UNIQUE (meal_plan_id, day_of_week, meal_type)
);

COMMENT ON TABLE  meal_slots                    IS 'Individual meal slots within a meal plan. One row per (plan, day, meal type) combination.';
COMMENT ON COLUMN meal_slots.day_of_week        IS '0 = Monday, 1 = Tuesday, …, 6 = Sunday.';
COMMENT ON COLUMN meal_slots.meal_type          IS 'One of: Frühstück, Mittagessen, Abendessen, Abendbrot. Abendbrot is a distinct cold-dinner type.';
COMMENT ON COLUMN meal_slots.is_locked          IS 'When TRUE, the regenerate-plan algorithm skips this slot and keeps the assigned recipe.';
COMMENT ON COLUMN meal_slots.servings_override  IS 'If set, overrides the recipe''s servings_default for this slot. NULL = use household size from preferences.';

CREATE TRIGGER meal_slots_updated_at
  BEFORE UPDATE ON meal_slots
  FOR EACH ROW EXECUTE FUNCTION set_updated_at();

-- Indexes
CREATE INDEX IF NOT EXISTS idx_meal_slots_meal_plan_id ON meal_slots (meal_plan_id);
CREATE INDEX IF NOT EXISTS idx_meal_slots_recipe_id    ON meal_slots (recipe_id);


