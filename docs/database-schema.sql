COMMENT ON COLUMN ingredients.shelf_life_days       IS 'Typical shelf life in days from purchase. NULL if highly variable.';
COMMENT ON COLUMN ingredients.is_seasonal           IS 'TRUE if this ingredient has a notable seasonal availability (e.g. Spargel, Erdbeeren).';
