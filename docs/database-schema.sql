-- =============================================================================
-- ROW LEVEL SECURITY
-- =============================================================================
-- Strategy:
--   • Recipes and ingredients are readable by all authenticated users.
--   • Admin-only writes on recipes / ingredients via JWT claim check.
--   • All household data (plans, lists, pantry) is scoped to household members.
--   • User rows and preferences are only accessible by the owning user.
-- =============================================================================

-- ---------------------------------------------------------------------------
-- RLS: households
-- ---------------------------------------------------------------------------

ALTER TABLE households ENABLE ROW LEVEL SECURITY;

-- Any authenticated user can see a household they belong to
CREATE POLICY "households_select_member"
  ON households FOR SELECT
  USING (
    id IN (
      SELECT household_id FROM users
      WHERE users.id = auth.uid()
    )
  );

-- Any authenticated user can create a new household (they become its owner)
CREATE POLICY "households_insert_authenticated"
  ON households FOR INSERT
  WITH CHECK (auth.uid() = created_by);

-- Only the household creator (owner) can update household details
CREATE POLICY "households_update_owner"
  ON households FOR UPDATE
  USING (created_by = auth.uid())
  WITH CHECK (created_by = auth.uid());

-- Only the household creator can delete it
CREATE POLICY "households_delete_owner"
  ON households FOR DELETE
  USING (created_by = auth.uid());


-- ---------------------------------------------------------------------------
-- RLS: users
-- ---------------------------------------------------------------------------

ALTER TABLE users ENABLE ROW LEVEL SECURITY;

-- Users can see their own profile
CREATE POLICY "users_select_self"
  ON users FOR SELECT
  USING (id = auth.uid());

-- Users in the same household can see each other's display_name (for "checked_by" attribution)
CREATE POLICY "users_select_same_household"
  ON users FOR SELECT
  USING (
    household_id IS NOT NULL
    AND household_id = (
      SELECT household_id FROM users AS me WHERE me.id = auth.uid()
    )
  );

-- Users can only update their own profile
CREATE POLICY "users_update_self"
  ON users FOR UPDATE
  USING (id = auth.uid())
  WITH CHECK (id = auth.uid());

-- Row is inserted automatically by the on_auth_user_created trigger (SECURITY DEFINER)
-- No manual INSERT policy needed for regular users.


