  ON meal_plans FOR UPDATE
  USING (
    household_id = (
      SELECT household_id FROM users WHERE users.id = auth.uid()
    )
  )
  WITH CHECK (
    household_id = (
      SELECT household_id FROM users WHERE users.id = auth.uid()
    )
  );

-- Only the plan creator can delete a plan
CREATE POLICY "meal_plans_delete_creator"
  ON meal_plans FOR DELETE
  USING (created_by = auth.uid());


